# Conditional Statements

# If
num = 20
if num > 10:
    print("Your number is greater than 10")

# IF Else
num = 3
if num > 10:
    print("Your number is greater than 10")

else:
    print("Your number is not greater than 10")

# IF ELSE IF
num = 3
if num > 10:
    print("your num is greater than 10")
elif num == 3:
    print("your num is 3")
else:
    print("your num is less than or equal to 10")


# Multiple Conditional
# and
# or
num = 30
if (num > 10) and (num < 100):
    print("Your number is greater than 10 but less than 100")
